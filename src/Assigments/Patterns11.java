package Assigments;

public class Patterns11 {
public static void main(String[] args) {
	int n=6;
			for(int i=3;i<=n;i++)
			{
				for(int j=3;j<=i;j++)
				{
					System.out.print(i);
				}
				System.out.println();
				
			}
			for(int i=n-1;i>=3;i--)
			{
				for(int j=3;j<=i;j++)
				{
					System.out.print(i);
				}
				System.out.println();
				
			}
}
}
