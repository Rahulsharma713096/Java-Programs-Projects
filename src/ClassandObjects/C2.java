package ClassandObjects;
class Dog1 {
	String name;
	String color;
	int cost;
}
public class C2 {
	public static void main(String[] args) {
		Dog1 d=new Dog1();
		d.name="JIMMY";
		d.color="BLACK";
		d.cost=10000;
		System.out.println(d.name);
		System.out.println(d.color);
		System.out.println(d.cost);
	}

}
